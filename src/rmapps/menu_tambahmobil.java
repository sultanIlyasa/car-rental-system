    private void bt_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_deleteActionPerformed
     try {
      String id=String.valueOf(tabelmobil.getValueAt(tabelmobil.getSelectedRow(),0));
      
      if (JOptionPane.showConfirmDialog(this, "Yakin Hapus", "Peringatan", JOptionPane.OK_CANCEL_OPTION)==JOptionPane.OK_OPTION) 
      {
        con.queryDelete("tb_mobil","id_mobil="+id);
        JOptionPane.showMessageDialog(this, "Penghapusan Berhasil");
      }
      else{
        return;
      }
      
     } catch (Exception e) {
       JOptionPane.showMessageDialog(this, "Pilih ID!");
       }
     loadTabel();
     clear();
    }//GEN-LAST:event_bt_deleteActionPerformed

    private void bt_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_updateActionPerformed
        try {
           if( !tf_merk.getText().isEmpty() 
               && !tf_tipe.getText().isEmpty() 
               && !tf_nopol.getText().isEmpty() 
               && !tf_sewa.getText().isEmpty()){
               String kolom[] = {"merk","tipe","tahun","nopol","harga","status"};
               String isi[] = { tf_merk.getText(),
                                tf_tipe.getText(),
                                cb_tahun.getSelectedItem().toString(),
                                tf_nopol.getText(),
                                tf_sewa.getText(),
                                cb_status.getSelectedItem().toString()};
               con.queryUpdate("tb_mobil", kolom, isi,"id_mobil='"
                                +String.valueOf(tabelmobil.getValueAt(tabelmobil.getSelectedRow(),0))+"'");
               JOptionPane.showMessageDialog(this, "Update Berhasil");
           }else{
                JOptionPane.showMessageDialog(this, "Data Pengisian Ada Yang Kosong");
           }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Update");
        }
        loadTabel();
        clear();
    }//GEN-LAST:event_bt_updateActionPerformed

    private void bt_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_cariActionPerformed
      if(tf_cari.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Isi Kolom Pencarian");
      }else{
        try {
         rs = con.querySelectAll("tb_mobil", 
                 "id_mobil LIKE '%"+tf_cari.getText()
                 +"%' OR merk LIKE '%"+tf_cari.getText()
                 +"%' OR tipe LIKE '%"+tf_cari.getText()
                 +"%' OR tahun LIKE '%"+tf_cari.getText()
                 +"%' OR nopol LIKE '%"+tf_cari.getText()
                 +"%' OR harga LIKE '%"+tf_cari.getText()
                 +"%' OR status LIKE '%"+tf_cari.getText()+"%'" );
                 tabelmobil.setModel(new database.resultsettable(rs));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Pencarian Error");
            }
       }
       clear();
    }//GEN-LAST:event_bt_cariActionPerformed

