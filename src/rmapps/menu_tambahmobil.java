    private void create() {
        try {
            if( !tf_merk.getText().isEmpty() 
                && !tf_tipe.getText().isEmpty() 
                && !tf_nopol.getText().isEmpty() 
                && !tf_sewa.getText().isEmpty()){
                String kolom[] = {"merk","tipe","tahun","nopol","harga","status"};
                String isi[] = { tf_merk.getText(),
                                 tf_tipe.getText(),
                                 cb_tahun.getSelectedItem().toString(),
                                 tf_nopol.getText(),
                                 tf_sewa.getText(),
                                 cb_status.getSelectedItem().toString()};
                System.out.println( con.queryInsert("tb_mobil",kolom,isi));
                JOptionPane.showMessageDialog(this, "Penambahan Berhasil");
            }else{
                JOptionPane.showMessageDialog(this, "Data Pengisian Ada Yang Kosong");
            } 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Input");
             System.out.println("Salah");
        }
        loadTabel();
        clear();
    }
    public void cekHak(){
        hak = lb_hak.getText();
         if (hak != null) {

          this.setVisible(false);
          if (hak.equals("Admin")) {
           menu_home h = new menu_home();
           h.setVisible(true);
           h.admin();
          } 
          else {
           menu_home h = new menu_home();
           h.setVisible(true);
           h.user();
          }
         } 
    }
    
