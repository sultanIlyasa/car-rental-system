    private void bt_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_addActionPerformed
      if (tf_nik.getText().isEmpty()
          || tf_nama.getText().isEmpty()
          || tf_alamatjl.getText().isEmpty()
          || tf_alamatrt.getText().isEmpty()
          || tf_alamatrw.getText().isEmpty()
          || tf_alamatkel.getText().isEmpty() 
          || tf_alamatkec.getText().isEmpty()
          || tf_alamatkota.getText().isEmpty()              
          || tf_notelp.getText().isEmpty ()
          || tf_email1.getText().isEmpty()
          || tf_email2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data Pengisian Belum Benar");
      } else {
            String nama     = tf_nama.getText();
            String nik      = tf_nik.getText();
            String alamat   = "Jl. " +tf_alamatjl.getText() +", RT "
                              +tf_alamatrt.getText() +" RW " 
                              +tf_alamatrw.getText() +", Kel. "
                              +tf_alamatkel.getText() +", Kec. "
                              +tf_alamatkec.getText() +", "
                              +tf_alamatkota.getText();
            String tlp      = tf_notelp.getText();
            String email    = tf_email1.getText()+tf_email2.getText();
            
            ShowNama.setText(nama);
            ShowNIK.setText(nik);
            ShowAlamat.setText(alamat);
            ShowNoTelp.setText(tlp);
            ShowEmail.setText(email);
            
            
            this.setVisible(false);
            menu_transaksi rm = new menu_transaksi();
            rm.setVisible(true);
            
        }
    }//GEN-LAST:event_bt_addActionPerformed

